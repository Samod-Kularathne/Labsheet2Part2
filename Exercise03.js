function AreaCircle(){
    const r = 7;
    const pi = 3.14;
    const area = pi*r*r;

    document.write("area of the circle is ",area);
}
function PerimetreCircle(){
    const r = 7;
    const pi = 3.14;
    const perimetre = 2*pi*r;

    document.write("perimetre of the circle is ",perimetre);

}
function VolCube(){
    const lengthOfaSide = 6;
    const vol = lengthOfaSide*lengthOfaSide*lengthOfaSide;

    document.write("volume of the cube = ",vol);

}
